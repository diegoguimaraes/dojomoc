public class Math {
    public int sum(int i, int j)
    {
        return i+j;
    }

    public int sub(int i,int j)
    {
        return i-j;
    }

    public int mul(int i,int j)
    {
        return j*i;
    }

    public float div(int i, int j)
    {
        return i/j;
    }
    
}
